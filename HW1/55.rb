A = 5
N = 7
p 'Дано число А и натуральное число N. Найти результат следующего выражения 1 - А + А*2 - А*3 + … + ((-1)**N)*А*N.'
p "A: #{A}" + ', ' + "N: #{N}"
p 'result : ' + (4..N).inject(1 - A + A * 2 - A * 3 ){ |sum, n| sum + ((-1)**n)*A*n }.to_s
p '______________________________________________________________________________________________'
